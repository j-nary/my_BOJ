#include <iostream>

using namespace std;

int value[102];
int dong[10004];
int main() {
    int n, k;
    cin >> n >> k;

    for (int i = 0; i < n; i++)
        cin >> value[i];
    
    int v;
    for (int i = 0; i < n; i++) {
        v = value[i];
        for (int j = 1; j <= k; j++) {
            if (v == j) dong[j]++;
            else if (v < j) dong[j] = dong[j] + dong[j-v];
        }
    }

    cout << dong[k] << endl;
}
